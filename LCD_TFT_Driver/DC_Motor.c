union FLAGBIT DC_Motor_FlagBits; //"DC_Motor 直流电机"相关标志位变量


unsigned int G_DC_SpeedCnt = 0; //DC_Motor 直流电机"1秒内CCR3采样到的频率累加器
unsigned int G_DC_Speed = 0;   //DC_Motor 直流电机"1秒内CCR3采样到的频率次数

unsigned char G_DC_WorkState = 0; //运行状态-DC_Motor 直流电机"



